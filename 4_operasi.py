#operasi dalam matematika

#penjumlahan
a = int(input("masukan nilai ke 1 :"))
b = int(input("masukan nilai ke 2 :"))

#operasi + - * / ** %
jumlah = a + b
print("PENJUMLAHAN",jumlah)

kurang = a - b
print("PENGURANGAN",kurang)

kali = a * b
print("PERKALIAN",kali)

bagi = a / b
print("PEMBAGIAN",bagi)

pangkat = a**b
print("PERPANGKATAN",pangkat)

sisa = a%b
print("PERPANGKATAN",sisa)

