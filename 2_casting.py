#casting = merubah tipe data
itc = 9
ukm = 9.5
print("int, data =", itc ," bertype",type(itc))
print("int, data =", ukm ," bertype",type(ukm))
print()

#int-float
data1 = float(itc)
print ("int-float, data1 =", itc ," bertype",type(data1))

#float-int
data2 = int(ukm)
print ("int-float, data2 =", ukm ," bertype",type(data2))

#float-int
data2 = int(ukm)
print ("int-float, data2 =", ukm ," bertype",type(data2))

